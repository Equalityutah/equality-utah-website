<!-- ENUMERATED CATEGORIES IN LAW MODAL -->

    $(function() {

  $('.categories-law').click(function() {

    $('.categories-law-flex-holder').fadeIn();

    $('.modal-background').fadeIn();

    e.stopPropagation();

  });

  $('.close-modal').click(function() {

      $('.categories-law-flex-holder').fadeOut();

      $('.modal-background').fadeOut();

  });  
  
  $('.modal-background').click(function() {

      $('.categories-law-flex-holder').fadeOut();

      $('.modal-background').fadeOut();

  }); 

  $(document).keydown(function (event) {

      if (event.keyCode == 27) {

          $('.categories-law-flex-holder').fadeOut();

          $('.modal-background').fadeOut();

      }

  });

});

